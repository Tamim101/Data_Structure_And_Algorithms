#include <stdio.h>
#include <string.h>
int hash_value(const char* value){
    int sum = 0;
    for(int i = 0; i < strlen(value);i++){
        sum += (int)value[i];
    }
    return sum % 10;
}

int main(){
    char value[] = "adfc";
    printf("%s is the hash asci number: %d",value,hash_value(value));
    return 0;
}
